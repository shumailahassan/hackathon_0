    def __init__(self, vault_path: str):
        self.vault_path = Path(vault_path)
        self.logger = self._setup_logger()
        self.setup_basic_schedule()

    def _setup_logger(self) -> logging.Logger:
        """Setup logger for the scheduler."""
        logger = logging.getLogger('scheduler')
        logger.setLevel(logging.INFO)

        # Create file handler
        file_handler = logging.FileHandler('scheduler.log')
        file_handler.setLevel(logging.INFO)

        # Create console handler
        console_handler = logging.StreamHandler(sys.stderr)
        console_handler.setLevel(logging.INFO)

        # Create formatter
        formatter = logging.Formatter(
            '%(asctime)s - %(name)s - %(levelname)s - %(message)s'
        )
        file_handler.setFormatter(formatter)
        console_handler.setFormatter(formatter)

        # Add handlers to logger
        if not logger.handlers:
            logger.addHandler(file_handler)
            logger.addHandler(console_handler)

        return logger

    def setup_basic_schedule(self):
        """Set up basic scheduled tasks for the AI Employee with error handling."""

        try:
            # Schedule daily business briefing
            schedule.every().day.at("08:00").do(self.run_daily_briefing)
            self.logger.info("Scheduled daily briefing at 08:00")

            # Schedule weekly business audit
            schedule.every().sunday.at("22:00").do(self.run_weekly_audit)
            self.logger.info("Scheduled weekly audit at 22:00 on Sundays")

            # Schedule social media posts
            schedule.every().monday.at("10:00").do(self.schedule_social_media_posts)
            schedule.every().wednesday.at("10:00").do(self.schedule_social_media_posts)
            schedule.every().friday.at("10:00").do(self.schedule_social_media_posts)
            self.logger.info("Scheduled social media posts at 10:00 on Mondays, Wednesdays, and Fridays")

            # Schedule report generation
            schedule.every().monday.at("09:00").do(self.generate_weekly_report)
            self.logger.info("Scheduled weekly report at 09:00 on Mondays")

            self.logger.info("Scheduler initialized with all tasks successfully")

        except Exception as e:
            self.logger.error(f"Failed to setup scheduler: {e}", exc_info=True)
            raise

